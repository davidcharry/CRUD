    private void Crear1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Crear1ActionPerformed
        int fila = TbCrePed.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) TbCrePed.getModel();
        String fechaPedido = (String) model.getValueAt(fila, 0);
        int idProducto = (int) model.getValueAt(fila, 1);
        int idCliente = (int) model.getValueAt(fila, 2);
        Pedidos c = new Pedidos(fechaPedido, idProducto, idCliente);
        if(this.CtPedidos.crearPedidos(c)){
            JOptionPane.showMessageDialog(this, "Registro agregado Correctamente");
        }else{
           JOptionPane.showMessageDialog(this, "No se pudo agregar el registro"); 
        }
        FCrear.setVisible(false);
        model.removeRow(fila);
        model.addRow(new Object[]{});
    }//GEN-LAST:event_Crear1ActionPerformed

