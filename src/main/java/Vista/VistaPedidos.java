    private void Actualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Actualizar1ActionPerformed
        int fila = TbActPed.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) TbActPed.getModel();
        int ID = Integer.parseInt((String) model.getValueAt(fila, 0));
        String fechaPedido = (String) model.getValueAt(fila, 1);
        int idProducto = Integer.parseInt((String) model.getValueAt(fila, 2));
        int idCliente = Integer.parseInt((String) model.getValueAt(fila, 3));
        Pedidos p = new Pedidos(ID, fechaPedido, idProducto, idCliente);
        if (this.CtPedidos.actualizarPedidos(p)) {
            FActualizar.setVisible(false);
            JOptionPane.showMessageDialog(this, "Registro Actualizado Correctamente");
        } else {
            FActualizar.setVisible(false);
            JOptionPane.showMessageDialog(this, "No se pudo actualizar el registro");
        }
        
        String consulta = "";
        if (this.CtPedidos.buscarPedidos(consulta) == null) {
        } else {
            TbActPed.setModel(CtPedidos.buscarPedidos(consulta));
            FActualizar.setVisible(true);
        }
    }//GEN-LAST:event_Actualizar1ActionPerformed

