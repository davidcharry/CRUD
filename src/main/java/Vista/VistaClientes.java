    private void Crear2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Crear2ActionPerformed
        int fila = TbCreClie.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) TbCreClie.getModel();
        String documentoCliente = (String) model.getValueAt(fila, 0);
        String nombreCliente = (String) model.getValueAt(fila, 1);
        String apellidoCliente = (String) model.getValueAt(fila, 2);
        String direccionCliente = (String) model.getValueAt(fila, 3);
        String correoCliente = (String) model.getValueAt(fila, 4);
        String telefonoCliente = (String) model.getValueAt(fila, 5);
        Clientes c = new Clientes(documentoCliente, nombreCliente, apellidoCliente, direccionCliente, correoCliente, telefonoCliente);
        if(this.CtClientes.crearClientes(c)){
            JOptionPane.showMessageDialog(this, "Registro agregado Correctamente");
        }else{
           JOptionPane.showMessageDialog(this, "No se pudo agregar el registro"); 
        }
        FCrear.setVisible(false);
        model.removeRow(fila);
        model.addRow(new Object[]{});
    }//GEN-LAST:event_Crear2ActionPerformed

