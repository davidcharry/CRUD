    private void Actualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Actualizar1ActionPerformed
        int fila = TbActClie.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) TbActClie.getModel();
        int ID = Integer.parseInt((String) model.getValueAt(fila, 0));
        String documentoCliente = (String) model.getValueAt(fila, 1);
        String nombreCliente = (String) model.getValueAt(fila, 2);
        String apellidoCliente = (String) model.getValueAt(fila, 3);
        String direccionCliente = (String) model.getValueAt(fila, 4);
        String correoCliente = (String) model.getValueAt(fila, 5);
        String telefonoCliente = (String) model.getValueAt(fila, 6);
        Clientes c = new Clientes(ID, documentoCliente, nombreCliente, apellidoCliente, direccionCliente, correoCliente, telefonoCliente);
        if (this.CtClientes.actualizarClientes(c)) {
            FActualizar.setVisible(false);
            JOptionPane.showMessageDialog(this, "Registro Actualizado Correctamente");
        } else {
            FActualizar.setVisible(false);
            JOptionPane.showMessageDialog(this, "No se pudo actualizar el registro");
        }
        
        String consulta = "";
        if (this.CtClientes.buscarClientes(consulta) == null) {
        } else {
            TbActClie.setModel(CtClientes.buscarClientes(consulta));
            FActualizar.setVisible(true);
        }
    }//GEN-LAST:event_Actualizar1ActionPerformed

