    private void Crear1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Crear1ActionPerformed
        int fila = TbCreDir.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) TbCreDir.getModel();
        String calleCarrera = (String) model.getValueAt(fila, 0);
        String numeroDireccion = (String) model.getValueAt(fila, 1);
        String barrio = (String) model.getValueAt(fila, 2);
        String ciudad = (String) model.getValueAt(fila, 3);
        Direcciones d = new Direcciones(calleCarrera, numeroDireccion, barrio, ciudad);
        if(this.CtDirecciones.crearDirecciones(d)){
            JOptionPane.showMessageDialog(this, "Registro agregado Correctamente");
        }else{
           JOptionPane.showMessageDialog(this, "No se pudo agregar el registro"); 
        }
        FCrear.setVisible(false);
        model.removeRow(fila);
        model.addRow(new Object[]{});
    }//GEN-LAST:event_Crear1ActionPerformed

