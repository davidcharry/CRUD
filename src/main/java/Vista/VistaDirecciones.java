    private void Actualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Actualizar1ActionPerformed
        int fila = TbActDir.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) TbActDir.getModel();
        int ID = Integer.parseInt((String) model.getValueAt(fila, 0));
        String calleCarrera = (String) model.getValueAt(fila, 1);
        String numeroDireccion = (String) model.getValueAt(fila, 2);
        String barrio = (String) model.getValueAt(fila, 3);
        String ciudad = (String) model.getValueAt(fila, 4);
        Direcciones d = new Direcciones(ID, calleCarrera, numeroDireccion, barrio, ciudad);
        if (this.CtDirecciones.actualizarDirecciones(d)) {
            FActualizar.setVisible(false);
            JOptionPane.showMessageDialog(this, "Registro Actualizado Correctamente");
        } else {
            FActualizar.setVisible(false);
            JOptionPane.showMessageDialog(this, "No se pudo actualizar el registro");
        }
        
        String consulta = "";
        if (CtDirecciones.buscarDirecciones(consulta) == null) {
        } else {
            TbActDir.setModel(CtDirecciones.buscarDirecciones(consulta));
            FActualizar.setVisible(true);
        }
    }//GEN-LAST:event_Actualizar1ActionPerformed

