    private void Crear1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Crear1ActionPerformed
        int fila = TbCrePro.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) TbCrePro.getModel();
        String numeroNit = (String) model.getValueAt(fila, 0);
        String razonSocial = (String) model.getValueAt(fila, 1);
        String telefono = (String) model.getValueAt(fila, 2);
        String correoElectronico = (String) model.getValueAt(fila, 3);
        String representanteLegal = (String) model.getValueAt(fila, 4);
        String sitioWeb = (String) model.getValueAt(fila, 5);
        int Direcciones_idDireccion = (int) model.getValueAt(fila, 6);
        Proveedor p = new Proveedor(numeroNit, razonSocial, telefono, correoElectronico, representanteLegal, sitioWeb, Direcciones_idDireccion);
        if (this.CtProveedor.crearProveedor(p)) {
            JOptionPane.showMessageDialog(this, "Registro agregado Correctamente");
        }else{
           JOptionPane.showMessageDialog(this, "No se pudo agregar el registro"); 
        }
        FCrear.setVisible(false);
        model.removeRow(fila);
        model.addRow(new Object[]{});
    }//GEN-LAST:event_Crear1ActionPerformed

