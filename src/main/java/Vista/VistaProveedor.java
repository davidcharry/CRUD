    private void Actualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Actualizar1ActionPerformed
        int fila = TbActPro.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) TbActPro.getModel();
        int ID = Integer.parseInt((String) model.getValueAt(fila, 0));
        String numeroNit = (String) model.getValueAt(fila, 1);
        String razonSocial = (String) model.getValueAt(fila, 2);
        String telefono = (String) model.getValueAt(fila, 3);
        String correoElectronico = (String) model.getValueAt(fila, 4);
        String representanteLegal = (String) model.getValueAt(fila, 5);
        String sitioWeb = (String) model.getValueAt(fila, 6);
        int Direcciones_idDireccion = Integer.parseInt((String) model.getValueAt(fila, 7));
        Proveedor p = new Proveedor(ID, numeroNit, razonSocial, telefono, correoElectronico, representanteLegal, sitioWeb, Direcciones_idDireccion);
        if (this.CtProveedor.actualizarProveedor(p)) {
            FActualizar.setVisible(false);
            JOptionPane.showMessageDialog(this, "Registro Actualizado Correctamente");
        } else {
            FActualizar.setVisible(false);
            JOptionPane.showMessageDialog(this, "No se pudo actualizar el registro");
        }
        
         String consulta = "";
        if (this.CtProveedor.buscarProveedor(consulta) == null) {
        } else {
            TbActPro.setModel(CtProveedor.buscarProveedor(consulta));
            FActualizar.setVisible(true);
        }
        
    }//GEN-LAST:event_Actualizar1ActionPerformed

